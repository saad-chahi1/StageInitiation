<h3 class="cen">COURS INDIVIDUELS ET COURS EN GROUPES : LES OBJECTIFS</h3>
<br><br>
    <p class="cen"><strong>Une étape souvent essentielle à la réussite scolaire</strong> </p>
    <br>
     <p class="cen">L'école est une étape obligatoire jusqu'à l'âge de 16 ans. Si c'est le cas, c'est pour une bonne raison. L'éducation scolaire de votre enfant est importante. Elle lui donne les bases nécessaires à sa réussite professionnelle.</p>
      <p class="cen">En effet, les cours donnés à l'école, au collège, au lycée, à l'université, dans les classes préparatoires ou en écoles d'ingénieurs posent un socle. Ce socle est une base solide donnant les outils nécessaires à la construction d'un avenir professionnel. C'est pourquoi la réussite scolaire est capitale pour votre enfant.</p>

<br><br>
 <div class="cent1">
    <p class="cent"><strong>La méthodologie au cœur de la formation</strong> </p>
    <br>
     <p class="cent">Il n'y a pas d'apprentissage efficace sans la mise en place d'une véritable méthodologie de travail. Elle est nécessaire pour que chaque effort produit par l'élève lui permette d'obtenir des résultats concrets.
La méthodologie est la base de tout travail efficace. Elle permet l'organisation et la mise en place des idées pour la compréhension et la réalisation d'un exercice. Il est ainsi nécessaire de travailler la méthodologie autant que le contenu d'une leçon.</p>
      <p class="cent">La premiere étape lors de la mise en place d'un cours de soutien sera de détecter les faiblesses méthodologiques de l'élève (mauvaise organisation, apprentissage par coeur, pas de passage entre la théorie et la mise en place au travers d'exercices d'application...) et de l'aider à se structurer.
En revanche, si la mise en place d'une méthodologie de travail est essentielle, elle ne permettra pas - à elle seule - de résoudre les difficultés scolaires et, plus l'élève sera avancé dans sa scolarité, plus le travail à fournir durant le cours particuliers ou en dehors de séances de cours sera important.</p>
    <p class="cent">Les cours en groupe passent par du résumé de cours, des exercices d'applications, des exercices d'approfondissements, une rédaction type à adapter pour enfin rendre l'élève autonome dans son travail grâce aux différentes astuces et scénarios type qu'il aura appris pendant les cours en groupe.</p></div>

     <div class="cent2">
    <p class="cent"><strong>Professeur de soutien: quelle finalité ?</strong> </p>
    <br>
     <p class="cent">Parfois, il est nécessaire de faire appel à un professeur pour recentrer les choses. Si votre enfant a des difficultés, il est toujours bon de contacter une personne qui a les compétences pour combler les lacunes.</p>
      <p class="cent">Il est important de comprendre que, dans la plupart des cas, votre enfant ne souhaite pas échouer. S'il ramène de mauvais résultats, c'est soit par manque de travail, soit à cause d'une lacune. Dans les deux cas, vous pouvez louer les services d'un professeur de soutien.</p>
    <p class="cent">Souvent, l'incompréhension d'une leçon ou d'une partie de cours est due à une mauvaise méthode de travail. Le soutien scolaire est un créneau horaire qui permettra à votre enfant de trouver une bonne méthode.</p>
    <p class="cent">De plus, un professeur est une personne qui a sa propre pédagogie et sa propre façon d'expliquer qui s'adapte au profil de l'élève. Ainsi, il apporte à votre enfant une nouvelle vision d'un problème. C'est un plus inestimable qui peut sauver l'année scolaire d'un élève ou d'un étudiant.</p></div>
 
<br><br>
    <p class="cen"><strong>Le soutien scolaire à domicile à tout âge</strong> </p>
    <br>
     <p class="cen">Le soutien scolaire à domicile est possible à tout âge. Il est d'ailleurs recommandé de contacter un professeur lorsque votre enfant est en difficulté. Il ne faut pas laisser s'installer une lacune sur le long terme ; elle risquerait de rester.</p>
      <p class="cen">C'est pourquoi il n'y a pas d'âge pour bénéficier du soutien scolaire. Il existe des professeurs qualifiés pour tous les niveaux.</p>
       <p class="cen">Les difficultés peuvent s'installer à tout moment. C'est souvent le cas lors du passage de l'école primaire au collège, du collège au lycée et du lycée aux études supérieures. L'enseignement change et devient de plus en plus spécialisé. De plus, la notion d'orientation voit le jour à la fin de la troisième ce qui pose une problématique supplémentaire.</p>

